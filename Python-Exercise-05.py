Name = ["alex" , "TIM" , "Christian" , "ryan" , "joHN" ]

print(Name)
# The output would be = ['alex', 'TIM', 'Christian', 'ryan', 'joHN']

print(Name[1].lower())
# The output would be = tim

print(Name[0].title())
# The output would be = Alex

print(Name[-1].lower())
# The output would be = john

print(Name[-2].title())
# The output would be = Ryan

print(Name[2].upper())
# The output would be = CHRISTIAN





